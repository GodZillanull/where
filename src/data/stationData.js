// 駅名検索（前方一致・部分一致）
export const searchStations = (query, limit = 10) => {
  if (!query || query.length === 0) return [];

  const normalizedQuery = query.replace(/駅$/, '').toLowerCase();

  // 前方一致を優先
  const prefixMatches = stations.filter(s =>
    s.toLowerCase().startsWith(normalizedQuery)
  );

  // 部分一致（前方一致に含まれないもの）
  const partialMatches = stations.filter(s =>
    s.toLowerCase().includes(normalizedQuery) && !s.toLowerCase().startsWith(normalizedQuery)
  );

  return [...prefixMatches, ...partialMatches].slice(0, limit);
};
